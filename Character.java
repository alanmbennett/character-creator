

public class Character {
    
    private String name, race, classType, gender;
    private double str, intel, dext, wis;
    
    public void setName(String name)
    {
        this.name = name;
    }
    
    public String getName()
    {
        return name;
    }
    
    public void setRace(String race)
    {
        this.race = race;
    }
    
    public String getRace()
    {
        return race;
    }
    
    public void setClassType(String classType)
    {
        this.classType = classType;
    }
    
    public String getClassType()
    {
        return classType;
    }
    
    public void setGender(String gender)
    {
        this.gender = gender;
    }
    
    public String getGender()
    {
        return gender;
    }
    
    public void setStrength(double str)
    {
        this.str = str;
    }
    
    public double getStrength()
    {
        return str;
    }
    
    public void setIntel(double intel)
    {
        this.intel = intel;
    }
    
    public double getIntel()
    {
        return intel;
    }
    
    public void setDext(double dext)
    {
        this.dext = dext;
    }
    
    public double getDext()
    {
        return dext;
    }
    
    public void setWisdom(double wis)
    {
        this.wis = wis;
    }
    
    public double getWisdom()
    {
        return wis;
    }
}
